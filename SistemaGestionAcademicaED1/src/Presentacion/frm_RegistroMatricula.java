    private void jbtnMatricularActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jbtnMatricularActionPerformed
        // TODO add your handling code here:
        try {
            if (!camposVacios()) {
                //datos alumno
                alumno.setNombre(jtxtNombre.getText());
                alumno.setApellido(jtxtApellido.getText());
                alumno.setDireccion(jtxtDireccion.getText());
                alumno.setDocumentoIdentidad(jtxtDni.getText().trim());
                alumno.setGenero((char) jcbxGenero.getSelectedItem().toString().charAt(0));
                alumno.setTelefono(jtxtTelefono.getText());
                //datos de la matricula
                matricula.setCosto(Float.parseFloat(jtxtCostoMatricula.getText()));
                matricula.setMetodoPago(jcbMetodoPago.getSelectedItem().toString());
                matricula.setPeriodoAcademico(jtxtPeriodoAcademico.getText());
                //datos del aula a la que pertencera el alumno
                controlMatricula.setAula(new Aula() {
                    {
                        setId(Integer.parseInt(jcbxAulasDisponibles.getSelectedItem().toString()));
                    }
                });
                //enviamos los datos a la clase controladora
                controlMatricula.setAlumno(alumno);
                controlMatricula.setMatricula(matricula);
                controlMatricula.matricularAlumno();
                mostrarUsuarioAlumno();
            } else {
                JOptionPane.showInternalMessageDialog(null, "Hay campos Vacios, completelos.");
            }
        } catch (Exception e) {
            JOptionPane.showMessageDialog(null, "Hubo un error:"+e.toString());
        }

    }//GEN-LAST:event_jbtnMatricularActionPerformed

