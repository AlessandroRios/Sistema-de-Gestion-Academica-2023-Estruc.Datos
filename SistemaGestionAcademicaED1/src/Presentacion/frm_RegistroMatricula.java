    private boolean camposVacios() {
        return jcbxSeccion.getSelectedIndex() == 0 || jcbxGenero.getSelectedIndex() == 0
                || jcbxGrado.getSelectedIndex() == 0 || jcbxNivel.getSelectedIndex() == 0
                || jcbxAulasDisponibles.getSelectedItem().equals("") || jtxtNombre.getText().equals("")
                || jtxtApellido.getText().equals("")
                || jtxtDni.getText().equals("") || jtxtDireccion.getText().equals("")
                || jtxtTelefono.getText().equals("") || jtxtCostoMatricula.getText().equals("")
                || jcbMetodoPago.getSelectedIndex()==0;
    }

    private void cargarAulasDisponibles() {
        if (jcbxGrado.getSelectedIndex() != 0 && jcbxNivel.getSelectedIndex() != 0 && jcbxSeccion.getSelectedIndex() != 0) {
            jcbxAulasDisponibles.setSelectedIndex(0);
            jcbxAulasDisponibles.removeAllItems();
            controlMatricula.aulasDisponibles(jcbxGrado.getSelectedIndex(),
                    jcbxNivel.getSelectedIndex(),
                    jcbxSeccion.getSelectedIndex());
        } else {
            JOptionPane.showInternalMessageDialog(null, "Seleccione un grado,nivel o seccion valido");
        }
    }

